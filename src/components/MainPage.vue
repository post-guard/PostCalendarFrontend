<template>
  <div>
  <a-layout style="min-height: 100vh">
    <a-layout-sider v-model:collapsed = "collapsed"
                    :collapsible = true
                    :defaultCollapsed = true>
      <div class="logo"/>

      <a-menu v-model:selectedKeys="selectedKeys"
              theme="dark"
              mode="inline"

              @select="menu_selected">

        <a-menu-item key="calendar">
          <calendar-outlined />
          <span>
            日程表
          </span>
        </a-menu-item>

        <a-menu-item key="map">
          <global-outlined />
          <span>
            地图
          </span>
        </a-menu-item>

        <a-menu-item key="home">
          <user-outlined />
          <span>
            个人主页
          </span>
        </a-menu-item>

      </a-menu>
    </a-layout-sider>


    <a-layout>
      <a-layout-header style="background: #fff; padding: 0" />
      <a-layout-content style="margin: 0 16px">

        <router-view>

        </router-view>

      </a-layout-content>
      <a-layout-footer style="text-align: center">
        PostCalendar ©2023 Created by PostCalendar Team
      </a-layout-footer>


    </a-layout>


  </a-layout>


    </div>
</template>

<script setup lang="ts">
import {CalendarOutlined,UserOutlined,GlobalOutlined} from "@ant-design/icons-vue";
import {ref, watch} from "vue";
import {useRouter} from "vue-router";

const router = useRouter();

const collapsed = ref(true);
//const defSelectedKey = Array(toRaw(router).currentRoute.value.fullPath.substring(1));
const selectedKeys = ref([""]);


watch(()=>router,(newValue)=>{
      //console.log(newValue.currentRoute.value.fullPath);
      selectedKeys.value[0] = newValue.currentRoute.value.fullPath.substring(1);
      //传进来的是子页面的router如/calendar,需要截去第一个字符
      },
    { immediate: true ,deep: true}
    //immediate负责首次监听即触发的功能，deep负责监听对象内部属性发生变化
);


function menu_selected(){

  //console.log(selectedKeys.value)
  switch (selectedKeys.value[0]){
    case 'calendar' :

      router.push({path:"/calendar"});

      break;

    case 'map':
      router.push({path:"/map"});
      break;

    case 'home':
      router.push({path:"/home"});
      break;

    default:

      router.push({path:"/calendar"});
  }
}
</script>

<style scoped>
.logo {
  height: 32px;
  margin: 16px;
  background: rgba(255, 255, 255, 0.3);
}


